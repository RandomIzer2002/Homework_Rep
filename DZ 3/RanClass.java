public class RanClass {
	int ranNumber;
	char ranCharacter;

	public void setNumber(int ranNumber) {
		this.ranNumber = ranNumber;
	}

	public int getNumber() {
		return ranNumber;
	}

	public void setCharacter(char ranCharacter) {
		this.ranCharacter = ranCharacter;
	}

	public char getCharacter() {
		return ranCharacter;
	}
}